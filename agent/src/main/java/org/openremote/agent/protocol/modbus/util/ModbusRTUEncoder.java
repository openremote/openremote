package org.openremote.agent.protocol.modbus.util;

import io.netty.buffer.ByteBuf;
import io.netty.channel.ChannelHandlerContext;
import io.netty.handler.codec.MessageToByteEncoder;

/**
 * Encodes Modbus RTU frames to bytes (Unit ID + PDU + CRC16)
 */
public class ModbusRTUEncoder extends MessageToByteEncoder<ModbusSerialFrame> {

    @Override
    protected void encode(ChannelHandlerContext ctx, ModbusSerialFrame frame, ByteBuf out) throws Exception {
        byte[] frameBytes = frame.pack();
        out.writeBytes(frameBytes);
    }
}
